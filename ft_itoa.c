#include "libft.h"

char	*ft_itoa(int n)
{
	char	*c;

	c = (char *)malloc(sizeof(char *));
	if (n >= 0)
	{
		while (n != 0)
		{
			c--;
			*c = '0' + (n % 10);
			n = n / 10;
		}
	}
	else
	{
		while (n != 0)
		{
			c--;
			*c = '0' - (n % 10);
			n = n / 10;
		}
		*--c = '-';
	}
	return (c);
}
